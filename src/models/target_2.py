import numpy as np


def f_2_center(y, params):
    m, j, b, A, yC = params.m, params.j, params.b, params.A, params.yC2

    yC1 = yC[0]
    yC2 = yC[1]

    f = np.zeros([18])
    f[0] = 1 / (m * j - b ** 2) * (j * y[3] - b * y[6])
    f[1] = 1 / (m * j - b ** 2) * (j * y[4] - b * y[7])
    f[2] = 1 / (m * j - b ** 2) * (j * y[5] - b * y[8])

    f[3] = -A * (y[0] - yC1[0]) / (
                (((y[0] - yC1[0]) ** 2 + (y[1] - yC1[1]) ** 2 + (y[2] - yC1[2]) ** 2)) ** (3 / 2)) + A * (
                       y[0] - y[9]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))
    f[4] = -A * (y[1] - yC1[1]) / (
                (((y[0] - yC1[0]) ** 2 + (y[1] - yC1[1]) ** 2 + (y[2] - yC1[2]) ** 2)) ** (3 / 2)) + A * (
                       y[1] - y[10]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))
    f[5] = -A * (y[2] - yC1[2]) / (
                (((y[0] - yC1[0]) ** 2 + (y[1] - yC1[1]) ** 2 + (y[2] - yC1[2]) ** 2)) ** (3 / 2)) + A * (
                       y[2] - y[11]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))

    f[6] = -b / (m * j - b ** 2) * (y[4] * y[8] - y[5] * y[7])
    f[7] = -b / (m * j - b ** 2) * (y[5] * y[6] - y[3] * y[8])
    f[8] = -b / (m * j - b ** 2) * (y[3] * y[7] - y[4] * y[6])

    f[9] = 1 / (m * j - b ** 2) * (j * y[12] - b * y[15])
    f[10] = 1 / (m * j - b ** 2) * (j * y[13] - b * y[16])
    f[11] = 1 / (m * j - b ** 2) * (j * y[14] - b * y[17])

    f[12] = -A * (y[9] - yC1[0]) / (
                (((y[9] - yC1[0]) ** 2 + (y[10] - yC1[1]) ** 2 + (y[11] - yC1[2]) ** 2)) ** (3 / 2)) - A * (
                        y[0] - y[9]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))
    f[13] = -A * (y[10] - yC1[1]) / (
                (((y[9] - yC1[0]) ** 2 + (y[10] - yC1[1]) ** 2 + (y[11] - yC1[2]) ** 2)) ** (3 / 2)) - A * (
                        y[1] - y[10]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))
    f[14] = -A * (y[11] - yC1[2]) / (
                (((y[9] - yC1[0]) ** 2 + (y[10] - yC1[1]) ** 2 + (y[11] - yC1[2]) ** 2)) ** (3 / 2)) - A * (
                        y[2] - y[11]) / ((((y[0] - y[9]) ** 2 + (y[1] - y[10]) ** 2 + (y[2] - y[11]) ** 2)) ** (3 / 2))

    f[15] = -b / (m * j - b ** 2) * (y[13] * y[17] - y[14] * y[16])
    f[16] = -b / (m * j - b ** 2) * (y[14] * y[15] - y[12] * y[17])
    f[17] = -b / (m * j - b ** 2) * (y[12] * y[16] - y[13] * y[15])