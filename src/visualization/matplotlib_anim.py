import numpy as np
import matplotlib.pyplot as plt
import matplotlib.animation as animation


# --- Настройка начальных условий ---
def simulate_particle_motion(alpha, n, m, L, A, num_frames):
    """
    Симуляция движения частицы.

    alpha: Параметр центрального потенциала
    n: Степень центрального потенциала (0 <= n < 2)
    m: Масса частицы
    L: Угловой момент частицы
    A: Амплитуда колебаний
    num_frames: Количество кадров для анимации
    """

    # 1. Определение радиуса круговой орбиты r0
    r0 = (L**2 / (alpha * n * m))**(1 / (2 - n))

    # 2. Частота малых колебаний
    omega = np.sqrt(alpha * n * (2 - n) / (m * r0**(n + 2)))

    # 3. Время и переменные
    t = np.linspace(0, 2 * np.pi / omega, num_frames)  # массив времени
    r = r0 + A * np.cos(omega * t)  # радиус в зависимости от времени
    theta = L / (m * r0**2) * t  # угловое движение (равномерное вращение)

    # 4. Преобразование в декартовы координаты
    x = r * np.cos(theta)
    y = r * np.sin(theta)

    return x, y, r0


# --- Анимация движения ---
def create_animation(x, y, r0, num_frames):
    """
    Создание анимации движения частицы.

    x: Массив координат x
    y: Массив координат y
    r0: Радиус круговой орбиты
    num_frames: Количество кадров
    """
    # Настройка графика
    fig, ax = plt.subplots()
    ax.set_xlim(-1.5 * r0, 1.5 * r0)
    ax.set_ylim(-1.5 * r0, 1.5 * r0)
    ax.set_aspect('equal', 'box')
    ax.grid(True)

    # Отрисовка радиуса r0
    circle = plt.Circle((0, 0), r0, color='gray', linestyle='--', fill=False, label="Круговая орбита (r0)")
    ax.add_artist(circle)
    ax.legend()

    # Объект частицы
    particle, = ax.plot([], [], 'ro', markersize=8)

    # Функция инициализации
    def init():
        particle.set_data([], [])
        return particle,

    # Функция обновления для каждого кадра
    def update(frame):
        particle.set_data([x[frame]], [y[frame]])
        return particle,

    # Создание анимации
    ani = animation.FuncAnimation(fig, update, frames=num_frames, init_func=init, blit=True, interval=50)

    # Сохранение анимации как GIF (опционально)
    ani.save("particle_motion.gif", writer="imagemagick", fps=30)

    plt.show()


# --- Основной код ---
if __name__ == "__main__":
    # Задать параметры
    alpha = 10     # Сила центрального потенциала
    n = 1        # Степень потенциала (0 <= n < 2)
    m = 10         # Масса частицы
    L = 5          # Угловой момент частицы
    A = 1        # Амплитуда малых отклонений
    num_frames = 500  # Количество кадров в анимации

    # Симуляция движения
    x, y, r0 = simulate_particle_motion(alpha, n, m, L, A, num_frames)

    # Создание анимации
    create_animation(x, y, r0, num_frames)

